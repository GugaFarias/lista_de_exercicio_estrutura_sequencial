#Faça um Programa que peça as 4 notas bimestrais e mostre a média.

numero1 = float(input("Favor digite o primeiro número: "))
numero2 = float(input("Favor digite o segundo número: "))
numero3 = float(input("Favor digite o segundo número: "))
numero4 = float(input("Favor digite o segundo número: "))

soma = numero1 + numero2 + numero3 + numero4
media = soma // 4

print("A media desses numeros é: ", media)